/* pe06.c -- variable management */
#include <stdio.h>
int main(void)
{
		//identify three variables
		int toes, twice_toes, toes_squared;
		//assingment
		toes = 10;
		twice_toes = 2 * toes;
		toes_squared = toes * toes;
		//print
		printf("toes is %d, and twice toes is %d, toes squared is %d.\n", toes, twice_toes, toes_squared);

		return 0;
}
